
/*Abstract Class
An abstract class is like a blueprint. It can define methods, but some of them might not have any code (just the name). You cannot create an object from an abstract class. It's meant to be extended by other classes that will fill in the details.

 Concrete Class
A concrete class is a complete class. It has full method definitions and can be used to create objects. If it extends an abstract class, it must provide the missing parts.
 Mixin
A mixin is used to add extra functionality to a class (like flying, singing, etc.). It’s not a class you extend — you use it with the keyword `with`. Mixins don’t have constructors, and they’re just for sharing code.

- Abstract class: defines what something should do.
- Concrete class: defines what something does.
- Mixin: adds extra abilities to a class.
